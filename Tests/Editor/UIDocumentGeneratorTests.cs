    public sealed class UIDocumentGeneratorTests
    {
        private readonly List<UnityEngine.Object> m_CreatedObjects = new List<UnityEngine.Object>();
        private readonly List<string> m_TempFolders = new List<string>();

        [TearDown]
        public void TearDown()
        {
            for (var i = 0; i < m_CreatedObjects.Count; i++)
            {
                UnityEngine.Object.DestroyImmediate(m_CreatedObjects[i]);
            }

            for (var i = 0; i < m_TempFolders.Count; i++)
            {
                if (Directory.Exists(m_TempFolders[i]))
                {
                    Directory.Delete(m_TempFolders[i], true);
                }
            }

            m_CreatedObjects.Clear();
            m_TempFolders.Clear();
        }

        [Test]
        public void Generate_WhenLocalizedTextBindingExists_EmitsRefreshAndSubscription()
        {
            var document = CreateTextDocument(out var text, new[] { "ui.title" });
            var folder = CreateTempFolder();

            InvokeGenerate(document, "Sample", folder);

            var generatedFolder = GetGeneratedFolder(folder);
            var logic = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "SampleWindow.cs"));
            var design = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "SampleWindow.Design.g.cs"));
            var model = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "SampleWindow.Model.g.cs"));

            StringAssert.Contains("public sealed partial class SampleWindow : UIWindow", logic);
            StringAssert.Contains("await InitializeDesignAsync();", logic);
            StringAssert.Contains("ReleaseDesign();", logic);
            StringAssert.Contains("using GameDeveloperKit.Localization;", design);
            StringAssert.Contains("[UIOption(\"Assets/UI/Test.prefab\", UILayer.Window)]", design);
            StringAssert.Contains("private global::UnityEngine.UI.Text text_title;", design);
            StringAssert.Contains("text_title = Document.GetComponent<global::UnityEngine.UI.Text>(\"b_Title\");", design);
            StringAssert.Contains("text_title = null;", design);
            StringAssert.Contains("private bool m_LocalizationSubscribed;", design);
            StringAssert.Contains("App.Localization.LocaleChanged += OnLocaleChanged;", design);
            StringAssert.Contains("private void RefreshLocalization()", design);
            StringAssert.Contains("text_title.text = App.Localization.GetText(\"ui.title\");", design);
            StringAssert.Contains("App.TryGetRegistered<LocalizationModule>(out var localization)", design);
            StringAssert.Contains("public sealed partial class SampleWindow", model);
            StringAssert.Contains("public sealed class Model", model);
            Assert.IsFalse(model.Contains("text_title"), model);
            Assert.IsFalse(model.Contains("global::UnityEngine.UI.Text"), model);
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(generatedFolder, "SampleModule.g.cs")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(generatedFolder, "SampleController.cs")));

            Assert.Less(
                design.IndexOf("SubscribeLocalization();", StringComparison.Ordinal),
                design.IndexOf("return UniTask.CompletedTask;", StringComparison.Ordinal));
            Assert.AreSame(text, document.LocalizedTexts[0].Component);
        }

