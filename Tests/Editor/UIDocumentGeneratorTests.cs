        [Test]
        public void Generate_WhenWindowNameContainsUnderscore_PreservesWindowTypeAndCreatesExpectedFiles()
        {
            var document = CreateTextDocument(out _, Array.Empty<string>());
            var folder = CreateTempFolder();
            System.IO.File.WriteAllText(Path.Combine(folder, "UI_ExampleWindow.Design.g.cs"), "// <auto-generated />\n// old design");
            System.IO.File.WriteAllText(Path.Combine(folder, "UI_ExampleWindow.Design.g.cs.meta"), "old design meta");
            System.IO.File.WriteAllText(Path.Combine(folder, "UI_ExampleWindow.Model.g.cs"), "// <auto-generated />\n// old model");
            System.IO.File.WriteAllText(Path.Combine(folder, "UI_ExampleWindow.Model.g.cs.meta"), "old meta");
            System.IO.File.WriteAllText(Path.Combine(folder, "UIExampleWindow.Model.g.cs"), "// <auto-generated />\n// legacy old model");
            System.IO.File.WriteAllText(Path.Combine(folder, "UIExampleWindow.Model.g.cs.meta"), "legacy old meta");

            InvokeGenerate(document, "UI_ExampleWindow", folder, "Assets/UI/UI_ExampleWindow.prefab");

            var generatedFolder = GetGeneratedFolder(folder, "UI_ExampleWindow");
            Assert.IsTrue(System.IO.File.Exists(Path.Combine(generatedFolder, "UI_ExampleWindow.cs")));
            Assert.IsTrue(System.IO.File.Exists(Path.Combine(generatedFolder, "UI_ExampleWindow.Design.g.cs")));
            Assert.IsTrue(System.IO.File.Exists(Path.Combine(generatedFolder, "UI_ExampleWindow.Model.g.cs")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UI_ExampleWindow.Design.g.cs")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UI_ExampleWindow.Design.g.cs.meta")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UI_ExampleWindow.Model.g.cs")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UI_ExampleWindow.Model.g.cs.meta")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UIExampleWindow.Model.g.cs")));
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(folder, "UIExampleWindow.Model.g.cs.meta")));

            var logic = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "UI_ExampleWindow.cs"));
            var design = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "UI_ExampleWindow.Design.g.cs"));
            var model = System.IO.File.ReadAllText(Path.Combine(generatedFolder, "UI_ExampleWindow.Model.g.cs"));

            StringAssert.Contains("public sealed partial class UI_ExampleWindow : UIWindow", logic);
            StringAssert.Contains("public sealed partial class UI_ExampleWindow", design);
            StringAssert.Contains("public sealed partial class UI_ExampleWindow", model);
            StringAssert.Contains("public sealed class Model", model);
            Assert.IsFalse(model.Contains("// old model"), model);
            Assert.IsFalse(model.Contains("text_title"), model);
        }

        [Test]
        public void Generate_WhenLogicFileExists_DoesNotOverwriteUserCode()
        {
            var document = CreateTextDocument(out _, Array.Empty<string>());
            var folder = CreateTempFolder();
            var logicPath = Path.Combine(folder, "SampleWindow.cs");
            System.IO.File.WriteAllText(logicPath, "// user code");

            InvokeGenerate(document, "Sample", folder);

            Assert.AreEqual("// user code", System.IO.File.ReadAllText(logicPath));
            var generatedFolder = GetGeneratedFolder(folder);
            Assert.IsFalse(System.IO.File.Exists(Path.Combine(generatedFolder, "SampleWindow.cs")));
            Assert.IsTrue(System.IO.File.Exists(Path.Combine(generatedFolder, "SampleWindow.Design.g.cs")));
            Assert.IsTrue(System.IO.File.Exists(Path.Combine(generatedFolder, "SampleWindow.Model.g.cs")));
        }

