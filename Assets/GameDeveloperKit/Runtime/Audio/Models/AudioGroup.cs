namespace GameDeveloperKit.Runtime
{
    /// <summary>
    /// 音频组枚举，用于分类管理不同类型的音频
    /// </summary>
    public enum AudioGroup
    {
        /// <summary>
        /// 主音量组
        /// </summary>
        Master = 0,

        /// <summary>
        /// 背景音乐组
        /// </summary>
        Bgm = 1,

        /// <summary>
        /// 音效组
        /// </summary>
        Sfx = 2,

        /// <summary>
        /// 语音组
        /// </summary>
        Voice = 3
    }
}
